using System;
using System.Collections.Generic;
using System.Text;

namespace SyncMuseWPF.Model.Spotify
{
    class SpotifySearchItemResponse
    {
        public SpotifySearchItemResponseTracks tracks { get; set; }
    }

    struct SpotifySearchItemResponseArtist
    {
        public ExternalUrls externalUrls { get; set; }
        public string href { get; set; }
        public string id { get; set; }
        public string name { get; set; }
        public string type { get; set; }
        public string uri { get; set; }
    }

    struct SpotifySearchItemResponseAlbum
    {
        public string album_type { get; set; }
        public List<Artist> artists { get; set; }
        public ExternalUrls externalUrls { get; set; }
        public string href { get; set; }
        public string id { get; set; }
        public List<Image> images { get; set; }
        public string name { get; set; }
        public string release_date { get; set; }
        public string release_date_precision { get; set; }
        public int total_tracks { get; set; }
        public string type { get; set; }
        public string uri { get; set; }
    }

    struct Artist2
    {
        public ExternalUrls externalUrls { get; set; }
        public string href { get; set; }
        public string id { get; set; }
        public string name { get; set; }
        public string type { get; set; }
        public string uri { get; set; }
    }

    struct Item
    {
        public SpotifySearchItemResponseAlbum album { get; set; }
        public List<Artist2> artists { get; set; }
        public int disc_number { get; set; }
        public int duration_ms { get; set; }
        public bool @explicit { get; set; }
        public ExternalIds externalIds { get; set; }
        public ExternalUrls externalUrls { get; set; }
        public string href { get; set; }
        public string id { get; set; }
        public bool is_local { get; set; }
        public bool is_playable { get; set; }
        public string name { get; set; }
        public int popularity { get; set; }
        public string preview_url { get; set; }
        public int track_number { get; set; }
        public string type { get; set; }
        public string uri { get; set; }
    }

    struct SpotifySearchItemResponseTracks
    {
        public string href { get; set; }
        public List<Item> items { get; set; }
        public int limit { get; set; }
        public string next { get; set; }
        public int offset { get; set; }
        public object previous { get; set; }
        public int total { get; set; }
    }
}
